                    case "error":
                        $("#form_getting_coupon #feedback").html("<div class='error'>" + data.message + "</div>").fadeIn("fast");
                        break;

                    case "success":
                        $("#form_getting_coupon #feedback").html("<div class='success'>" + data.message + "</div>").fadeIn("fast");

